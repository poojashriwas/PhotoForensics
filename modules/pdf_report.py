def add_summary_page(story, styles, summary):

    story.append(

        Paragraph(

            "<b><font color='#0d6efd'>EXECUTIVE SUMMARY</font></b>",

            styles["Title"]

        )

    )

    story.append(Spacer(1,20))

    table = Table(

        [

            ["Overall Risk", summary["risk"]],

            ["Investigation Score", f"{summary['score']}%"],

            ["Confidence", f"{summary['confidence']}%"],

            ["Report ID", summary["report_id"]]

        ],

        colWidths=[2.8*inch,3.2*inch]

    )

    table.setStyle(TableStyle([

        ("BACKGROUND",(0,0),(0,-1),colors.HexColor("#198754")),

        ("TEXTCOLOR",(0,0),(0,-1),colors.white),

        ("BACKGROUND",(1,0),(1,-1),colors.beige),

        ("GRID",(0,0),(-1,-1),1,colors.black),

        ("BOTTOMPADDING",(0,0),(-1,-1),10),

        ("FONTNAME",(0,0),(-1,-1),"Helvetica-Bold")

    ]))

    story.append(table)

    story.append(Spacer(1,30))

    story.append(

        Paragraph(

            "<b>Investigation Findings</b>",

            styles["Heading2"]

        )

    )

    story.append(Spacer(1,10))

    for reason in summary["reasons"]:

        story.append(

            Paragraph(

                "• " + reason,

                styles["BodyText"]

            )

        )

    story.append(PageBreak())

def generate_pdf_report(

    summary,
    image_path,
    file_info,
    metadata,
    gps,
    hashing,
    ela,
    quality,
    histogram,
    noise,
    ai_summary

):

    os.makedirs("static/reports", exist_ok=True)

    filename = (
        "Report_" +
        datetime.now().strftime("%Y%m%d_%H%M%S") +
        ".pdf"
    )

    output_path = os.path.join(
        "static",
        "reports",
        filename
    )

    doc = SimpleDocTemplate(output_path)

    styles = getSampleStyleSheet()

    story = []

    # -----------------------------
    # Cover Page
    # -----------------------------

    add_cover_page(
        story,
        styles,
        summary
    )

    # -----------------------------
    # Executive Summary
    # -----------------------------

    add_summary_page(
        story,
        styles,
        summary
    )

    # -----------------------------
    # Build PDF
    # -----------------------------

    doc.build(
    story,
    onFirstPage=add_page_number,
    onLaterPages=add_page_number
)

    return output_path
