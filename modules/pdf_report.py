from reportlab.platypus import (
    SimpleDocTemplate,
    Paragraph,
    Spacer,
    Table,
    TableStyle,
    PageBreak
)

from reportlab.lib import colors
from reportlab.lib.styles import getSampleStyleSheet
from reportlab.lib.enums import TA_CENTER
from reportlab.lib.units import inch

from datetime import datetime
from reportlab.platypus import Image

from reportlab.lib.enums import TA_LEFT

import os

from reportlab.lib.colors import HexColor

def add_page_number(canvas, doc):

    canvas.saveState()

    width, height = doc.pagesize

    # Header
    canvas.setFillColor(HexColor("#0d6efd"))
    canvas.rect(0, height-35, width, 35, fill=1)

    canvas.setFillColor(HexColor("#FFFFFF"))
    canvas.setFont("Helvetica-Bold", 14)
    canvas.drawString(
        30,
        height-22,
        "PHOTO FORENSICS INVESTIGATION REPORT"
    )

    # Footer
    canvas.setFillColor(HexColor("#666666"))
    canvas.setFont("Helvetica",9)

    canvas.drawString(
        30,
        15,
        "Generated by Photo Forensics Dashboard"
    )

    canvas.drawRightString(
        width-30,
        15,
        f"Page {doc.page}"
    )

    canvas.restoreState()

# =====================================
# Cover Page
# =====================================

def add_cover_page(story, styles, summary):

    title = styles["Title"]
    title.alignment = TA_CENTER

    heading = styles["Heading2"]
    heading.alignment = TA_CENTER

    story.append(Spacer(1,80))

    story.append(

        Paragraph(

            "<font color='#0d6efd'><b>PHOTO FORENSICS</b></font>",

            title

        )

    )

    story.append(

        Paragraph(

            "<b>INVESTIGATION REPORT</b>",

            title

        )

    )

    story.append(Spacer(1,35))

    table = Table(

        [

            ["Report ID", summary["report_id"]],

            ["Overall Risk", summary["risk"]],

            ["Investigation Score", f"{summary['score']}%"],

            ["Confidence", f"{summary['confidence']}%"],

            ["Generated", datetime.now().strftime("%d %B %Y")]

        ],

        colWidths=[2.8*inch,3*inch]

    )

    table.setStyle(TableStyle([

        ("BACKGROUND",(0,0),(0,-1),colors.HexColor("#0d6efd")),

        ("TEXTCOLOR",(0,0),(0,-1),colors.white),

        ("BACKGROUND",(1,0),(1,-1),colors.whitesmoke),

        ("GRID",(0,0),(-1,-1),1,colors.grey),

        ("ALIGN",(0,0),(-1,-1),"CENTER"),

        ("FONTNAME",(0,0),(-1,-1),"Helvetica-Bold"),

        ("BOTTOMPADDING",(0,0),(-1,-1),10)

    ]))

    story.append(table)

    story.append(Spacer(1,60))

    story.append(

        Paragraph(

            "<b>Digital Image Investigation Dashboard</b>",

            heading

        )

    )

    story.append(

        Paragraph(

            "Automatically Generated Investigation Report",

            styles["BodyText"]

        )

    )

    story.append(PageBreak())

